package com.desenvolvatec.sql2xquery.xquery;

public class XQJoinCondition {

    boolean notOperator = false; // é o mais comum, previne LEFT e RIGHT JOINs
    String tableName = null;
    
    public XQJoinCondition(String tableName){
        this.tableName = tableName;
        this.notOperator = false;
    }

    public XQJoinCondition(String tableName,boolean notOperator){
        this.tableName = tableName;
        this.notOperator = notOperator;
    }

}
