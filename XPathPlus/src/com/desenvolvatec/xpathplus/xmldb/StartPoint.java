package com.desenvolvatec.xpathplus.xmldb;

public class StartPoint {
    private String expression;
    private boolean filter;
    private boolean print;
    private int base; // O (INSTANCE), 1 (TAXONOMY) ou 2 (OPERATOR_DEFINITION)

    public final static int INSTANCE = 0;
    public final static int TAXONOMY = 1;
    public final static int OPERATOR_DEFINITION = 2; // subsídio para aproveitar a ocnfiguração para a base do operator definition

    public void setExpression(String expression) {
        this.expression = expression;
    }

    public String getExpression() {
        return expression;
    }

    public void setFilter(boolean filter) {
        this.filter = filter;
    }

    public boolean isFilter() {
        return filter;
    }

    public void setPrint(boolean print) {
        this.print = print;
    }

    public boolean isPrint() {
        return print;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getBase() {
        return base;
    }
}
